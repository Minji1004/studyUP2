package com.sp.teacher.notice;

public class Reply {

	private int tnotice_r_num;
	private int tnoticeNum;
	private int answer;
	private String userId;
	private String created;
	private String content;
	private String nickname;
	private String picture;
	
	public int getTnotice_r_num() {
		return tnotice_r_num;
	}
	public void setTnotice_r_num(int tnotice_r_num) {
		this.tnotice_r_num = tnotice_r_num;
	}
	public int getTnoticeNum() {
		return tnoticeNum;
	}
	public void setTnoticeNum(int tnoticeNum) {
		this.tnoticeNum = tnoticeNum;
	}
	public int getAnswer() {
		return answer;
	}
	public void setAnswer(int answer) {
		this.answer = answer;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	
	
	
}
