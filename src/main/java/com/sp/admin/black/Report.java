package com.sp.admin.black;

public class Report {
	private int reportNum;
	private String reportBoard;
	private int reportBoardNum;
	private int replNum;
	private int reportUser;
	private int reportedUser;
	private String reportCause;
	private int reportType;
	private String reportDate;
	
	public int getReportNum() {
		return reportNum;
	}
	public void setReportNum(int reportNum) {
		this.reportNum = reportNum;
	}
	public String getReportBoard() {
		return reportBoard;
	}
	public void setReportBoard(String reportBoard) {
		this.reportBoard = reportBoard;
	}
	public int getReportBoardNum() {
		return reportBoardNum;
	}
	public void setReportBoardNum(int reportBoardNum) {
		this.reportBoardNum = reportBoardNum;
	}
	public int getReplNum() {
		return replNum;
	}
	public void setReplNum(int replNum) {
		this.replNum = replNum;
	}	
	public String getReportCause() {
		return reportCause;
	}
	public void setReportCause(String reportCause) {
		this.reportCause = reportCause;
	}
	public int getReportType() {
		return reportType;
	}
	public void setReportType(int reportType) {
		this.reportType = reportType;
	}
	public String getReportDate() {
		return reportDate;
	}
	public void setReportDate(String reportDate) {
		this.reportDate = reportDate;
	}
	public int getReportUser() {
		return reportUser;
	}
	public void setReportUser(int reportUser) {
		this.reportUser = reportUser;
	}
	public int getReportedUser() {
		return reportedUser;
	}
	public void setReportedUser(int reportedUser) {
		this.reportedUser = reportedUser;
	}	
}
