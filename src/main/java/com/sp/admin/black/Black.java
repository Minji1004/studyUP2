package com.sp.admin.black;

public class Black {
	private int blackNum;
	private int blackUserNum;
	private String userId;
	private String nickName;
	private String tel;
	private String blackStart;
	private String blackEnd;
	
	public int getBlackNum() {
		return blackNum;
	}
	public void setBlackNum(int blackNum) {
		this.blackNum = blackNum;
	}
	public int getBlackUserNum() {
		return blackUserNum;
	}
	public void setBlackUserNum(int blackUserNum) {
		this.blackUserNum = blackUserNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getBlackStart() {
		return blackStart;
	}
	public void setBlackStart(String blackStart) {
		this.blackStart = blackStart;
	}
	public String getBlackEnd() {
		return blackEnd;
	}
	public void setBlackEnd(String blackEnd) {
		this.blackEnd = blackEnd;
	}
	
}
